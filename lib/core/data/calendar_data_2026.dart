  /// Returns the [DailyReading] for the given [date], or null if not found.
  static DailyReading? getReading(DateTime date) {
    if (date.year != 2026) return null;
    final monthData = months.firstWhere(
      (m) => m.month == date.month,
      orElse: () => const MonthReadings(month: -1, monthName: '', readings: []),
    );
    if (monthData.month == -1) return null;
    try {
      return monthData.readings.firstWhere((r) => r.day == date.day);
    } catch (_) {
      return null;
    }
  }

  /// Returns all days marked as special (feast days, holy days, etc.).
  static List<MapEntry<DateTime, DailyReading>> getFeastDays() {
    final result = <MapEntry<DateTime, DailyReading>>[];
    for (final month in months) {
      for (final reading in month.readings) {
        if (reading.isSpecialDay) {
          result.add(
            MapEntry(DateTime(2026, month.month, reading.day), reading),
          );
        }
      }
    }
    return result;
  }

  /// Returns the liturgical color for the given [date], or null if not set.
  static String? getLiturgicalColorForDate(DateTime date) {
    return getReading(date)?.liturgicalColor;
  }
